def rec_search array, element
    
    if array.length <= 1
        return array[0] == element
    end
          
    x = array.length/2
    
    if array[x] > element
        rec_search array.slice(0, x), element
    else
        rec_search array.slice(x, array.length - 1), element
    end
 
end
   
def search array, element
    x = 0 
    
    while x < array.length
        if array[x] == element
            return true
        end
        x+=1
    end
    false
end
 
